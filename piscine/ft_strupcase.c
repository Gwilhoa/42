#include <stdio.h>
char	*ft_strupcase(char *str)
{
	int		i;

	i = -1;
	while (str[++i])
	{
		if (str[i] > 96 && str[i] < 123)
		{
			str[i] = str[i] - 32;
		}
	}
	return (str);
}
